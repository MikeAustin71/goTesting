func testMultipleJdnGregorianJdnConversions(ePrefix string) error {

	ePrefix += "testMultipleJdnGregorianJdnConversions() "
	var err, err2 error
	var testData []JDNConversionTestDto

	testData, err = getGregorianJDNTestData(ePrefix)

	if err != nil {
		return err
	}

	floatFmtStr := "%46.30f"
	roundToDecPlaces := uint(30)
	var expectedIsLeapYear, actualIsLeapYear bool
	var compareResult, numOfTests int
	var expectedGregorianDateTime, actualGregorianDateTime datetime.ADateTimeDto
	var julianDayNoDto datetime.JulianDayNoDto
	var jdnConversionStartTime, jdnConversionEndTime,
		dateConversionStartTime, dateConversionEndTime,
		executionStartTime, executionEndTime time.Time

	executionStartTime = time.Now()

	var jdnConversionTotalTime, dateConversionTotalTime int64
	var actualJulianDayNoTime *big.Float
	var actualJulianDayNoTimeStr, outputStr string

	calGreg := datetime.CalendarGregorianUtility{}

	baseOutStrLen := 84
	successSep := strings.Repeat("*", baseOutStrLen)
	failureSep := strings.Repeat("&", baseOutStrLen)

	numOfTests = len(testData)

	for i := 0; i < numOfTests; i++ {

		jdnConversionStartTime = time.Now()

		julianDayNoDto,
			err2 = calGreg.GetJulianDayNumber(
			testData[i].Year,
			testData[i].Month,
			testData[i].Day,
			testData[i].Hour,
			testData[i].Minute,
			testData[i].Second,
			testData[i].Nanosecond,
			ePrefix)

		jdnConversionEndTime = time.Now()

		if err2 != nil {
			err = fmt.Errorf("%v\n"+
				"Error Returned from DateTimeStr: %v\n"+
				"i=%v\n",
				err2.Error(),
				testData[i].DateTimeStr,
				i)
			return err
		}

		jdnConversionTotalTime +=
			int64(jdnConversionEndTime.Sub(jdnConversionStartTime))

		actualJulianDayNoTime, err2 =
			julianDayNoDto.GetRoundedDayNoTimeBigFloat(roundToDecPlaces, ePrefix)

		if err2 != nil {
			err = fmt.Errorf("%v\n"+
				"Error Returned from GetRoundedDayNoTimeBigFloat\n"+
				"testData[%v].DateTimeStr='%v'\n"+
				err2.Error(),
				i,
				testData[i].DateTimeStr)
			return err
		}

		actualJulianDayNoTimeStr =
			fmt.Sprintf(floatFmtStr, actualJulianDayNoTime)

		if actualJulianDayNoTimeStr != testData[i].RoundedJulianDayNoTimeStr {
			outputStr = fmt.Sprintf(failureSep + "\n")
			outputStr +=
				fmt.Sprintf("Calc Error: Actual Julian Day No NOT EQUAL to Expected Julian Day No!\n"+
					"  Expected Julian Day No: %v\n"+
					"    Actual Julian Day No: %v\n"+
					"testData[%v].DateTimeStr: '%v'\n",
					testData[i].RoundedJulianDayNoTimeStr,
					actualJulianDayNoTimeStr,
					i,
					testData[i].DateTimeStr)
			outputStr += fmt.Sprintf(failureSep)
			outputStr += fmt.Sprintln()

			err = fmt.Errorf(ePrefix+"\n"+
				"%v", outputStr)
			return err
		}

		julianDayNoDto, err2 =
			datetime.JulianDayNoDto{}.NewFromJulianDayNoTime(
				actualJulianDayNoTime,
				false,
				ePrefix)

		if err2 != nil {
			err = fmt.Errorf("%v\n"+
				"Error Returned from NewFromJulianDayNoTime\n"+
				"testData[%v].DateTimeStr='%v'\n"+
				err2.Error(),
				i,
				testData[i].DateTimeStr)
			return err
		}

		expectedGregorianDateTime, err2 =
			datetime.ADateTimeDto{}.New(
				datetime.CalSpec.Gregorian(),
				testData[i].Year,
				datetime.CalYearType.Astronomical(),
				testData[i].Month,
				testData[i].Day,
				false,
				testData[i].Hour,
				testData[i].Minute,
				testData[i].Second,
				testData[i].Nanosecond,
				"UTC",
				"",
				"",
				ePrefix)

		if err2 != nil {
			err = fmt.Errorf("%v\n"+
				"Error Returned from ADateTimeDto{}.New()\n"+
				"testData[%v].DateTimeStr='%v'\n"+
				err2.Error(),
				i,
				testData[i].DateTimeStr)
			return err
		}

		dateConversionStartTime = time.Now()

		actualGregorianDateTime,
			err2 = calGreg.DateTimeFromJulianDateTime(
			julianDayNoDto,
			ePrefix)

		dateConversionEndTime = time.Now()

		if err2 != nil {
			err = fmt.Errorf(ePrefix+"\n"+
				"Error Returned from calGreg.DateTimeFromJulianDateTime()\n"+
				"testData[%v].DateTimeStr='%v'\n"+
				err2.Error(),
				i,
				testData[i].DateTimeStr)
			return err
		}

		err = actualGregorianDateTime.IsValidInstanceError(ePrefix +
			"actualGregorianDateTime.IsValidInstanceError() ")

		if err != nil {
			return err
		}

		dateConversionTotalTime +=
			int64(dateConversionEndTime.Sub(dateConversionStartTime))

		compareResult, err2 = expectedGregorianDateTime.Compare(&actualGregorianDateTime, ePrefix)

		expectedGregorianDateTime.SetTag("expectedGregorianDateTime")

		actualGregorianDateTime.SetTag("actualGregorianDateTime")

		expectedIsLeapYear = expectedGregorianDateTime.GetIsLeapYear()

		actualIsLeapYear =
			actualGregorianDateTime.GetIsLeapYear()

		if compareResult == 0 &&
			expectedIsLeapYear != actualIsLeapYear {
			outputStr = "\n"
			outputStr += fmt.Sprintln(failureSep)
			outputStr += fmt.Sprintln("                      ??PARTIAL SUCCESS??                             ")
			outputStr += fmt.Sprintln("   Expected Gregorian Date Time MATCHES Actual Gregorian Date Time!   ")
			outputStr += fmt.Sprintln("                 HOWEVER: 'isLeapYear' DOES NOT MATCH                 ")
			outputStr += fmt.Sprintf("testData[%v].DateTimeStr='%v'\n",
				i, testData[i].DateTimeStr)
			outputStr += fmt.Sprintf("testData[%v].DateStr='%v'\n",
				i, testData[i].DateStr)
			outputStr += CompareTwoDateTimeDtos(
				"Expected",
				expectedGregorianDateTime,
				"Actual",
				actualGregorianDateTime)

			outputStr += fmt.Sprintln(failureSep)

			err = fmt.Errorf(ePrefix+"\n"+
				"%v\n", outputStr)

			return err

		} else if compareResult == 0 {

			fmt.Printf("Test No. %4d SUCCESS: %v\n",
				i+1,
				testData[i].DateTimeJdnNoTimeStr)

		} else {
			outputStr = "\n"
			outputStr += fmt.Sprintln(failureSep)
			outputStr += fmt.Sprintln("                                   FAILURE                                   ")
			outputStr += fmt.Sprintln("   Expected Gregorian Date Time DOES NOT MATCH Actual Gregorian Date Time!   ")
			outputStr += fmt.Sprintln(failureSep)

			outputStr += fmt.Sprintf("testData[%v].DateStr='%v'\n",
				i, testData[i].DateStr)

			outputStr += fmt.Sprintf("testData[%v].DateTimeStr='%v'\n",
				i, testData[i].DateTimeStr)

			outputStr += fmt.Sprintf("      testData[%v].JulianDayNoTimeStr='%v'\n",
				i, testData[i].JulianDayNoTimeStr)

			jdnBigFloat, _ := julianDayNoDto.GetDayNoTimeBigFloat(ePrefix)

			outputStr += fmt.Sprintf("Last Julian Day No Submitted= "+floatFmtStr+"\n",
				jdnBigFloat)

			outputStr += CompareTwoDateTimeDtos(
				"Expected",
				expectedGregorianDateTime,
				"Actual",
				actualGregorianDateTime)
			outputStr += fmt.Sprintln(failureSep)
			err = fmt.Errorf(ePrefix+"\n"+
				"%v\n", outputStr)

			return err
		}
	}

	executionEndTime = time.Now()

	jdnConversionTotalTime = jdnConversionTotalTime / int64(numOfTests)

	elapsedTimeStr := GetElapsedTime(time.Duration(jdnConversionTotalTime))

	fmt.Println(successSep)
	fmt.Printf("       Average JDN Calculation Time: %v\n",
		elapsedTimeStr)

	dateConversionTotalTime = dateConversionTotalTime / int64(numOfTests)

	elapsedTimeStr = GetElapsedTime(time.Duration(dateConversionTotalTime))

	fmt.Printf("Average JDN To Date Conversion Time: %v\n",
		elapsedTimeStr)

	elapsedTimeStr = GetElapsedTime(executionEndTime.Sub(executionStartTime))

	fmt.Printf("Execution Elapsed Time: %v\n",
		elapsedTimeStr)
	fmt.Println(successSep)

	return err
}

func getMonthDayFromGregorianOrdDayNo(ePrefix string) error {

	ePrefix += "getMonthDayFromGregorianOrdDayNo() "

	gregCalData := datetime.CalendarGregorianBaseData{}

	yearNo := int64(-4721)

	var isLeapYear bool
	var err error

	isLeapYear,
		err = gregCalData.IsLeapYear(
		yearNo,
		datetime.CalYearType.Astronomical(),
		ePrefix)

	ordinalDate := 59

	expectedMonth := 2

	expectedDay := 28

	var yearAdjustment, actualMonth, actualDay int

	yearAdjustment,
		actualMonth,
		actualDay,
		err = gregCalData.GetMonthDayFromOrdinalDayNo(
		ordinalDate,
		isLeapYear,
		ePrefix)

	if err != nil {
		return fmt.Errorf("ordinalDate='%v'\n"+
			"isLeapYear='%v'\n"+
			"Error='%v'\n",
			ordinalDate, isLeapYear, err.Error())
	}

	fmt.Println("    " + ePrefix)
	fmt.Printf("        Test Year: %v\n",
		yearNo)
	fmt.Printf("Test Ordinal Date: %v\n",
		ordinalDate)
	fmt.Printf("   Expected Month: %v\n",
		expectedMonth)
	fmt.Printf("     Expected Day: %v\n",
		expectedDay)
	fmt.Printf("       isLeapYear: '%v'\n", isLeapYear)
	fmt.Println("---------------------------------------")
	fmt.Printf("     Actual Month: %v\n",
		actualMonth)
	fmt.Printf("       Actual Day: %v\n",
		actualDay)
	fmt.Printf("  Year Adjustment: %v\n",
		yearAdjustment)
	fmt.Println("---------------------------------------")
	if expectedMonth == actualMonth &&
		expectedDay == actualDay {
		fmt.Println("SUCCESS: Month and Day Match!")
	} else {
		fmt.Println("FAILURE: Month and Day DO NOT MATCH!!!!!")
	}
	fmt.Println("---------------------------------------")
	return nil
}


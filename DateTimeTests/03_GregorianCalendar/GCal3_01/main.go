func CompareTwoDateTimeDtos(
	expectedLabel string,
	expectedDateTime datetime.ADateTimeDto,
	actualLabel string,
	actualDateTime datetime.ADateTimeDto) string {

	expectedLabel = strings.TrimLeft(expectedLabel, " ")
	expectedLabel = strings.TrimRight(expectedLabel, " ")

	actualLabel = strings.TrimLeft(actualLabel, " ")
	actualLabel = strings.TrimRight(actualLabel, " ")

	spacerLen1 := 30 - len(expectedLabel)

	var spacer1, spacer2 string

	if spacerLen1 < 0 {
		spacer1 = ""
	} else if spacerLen1 < 2 {
		spacerLen1 = 0
		spacer1 = " "
	} else {
		spacer1 = strings.Repeat("-", spacerLen1/2)
	}

	spacerLen2 := 30 - len(actualLabel)

	if spacerLen2 < 0 {
		spacer2 = ""
	} else if spacerLen2 < 2 {
		spacerLen2 = 0
		spacer2 = " "
	} else {
		spacer2 = strings.Repeat("-", spacerLen2/2)
	}

	expectedLabel = " " + expectedLabel + " "
	actualLabel = " " + actualLabel + " "

	outputStr := "\n"
	outputStr += strings.Repeat("-", 64)
	outputStr += "\n"
	outputStr +=
		fmt.Sprintf(spacer1 + expectedLabel + spacer1 + spacer2 + actualLabel + spacer2 + "\n")

	outputStr += " " + expectedDateTime.GetTag() + "           " + actualDateTime.GetTag() + "\n"

	outputStr += fmt.Sprintf("         isLeapYear:   %-6v        %-6v\n",
		expectedDateTime.GetIsLeapYear(),
		actualDateTime.GetIsLeapYear())

	outputStr += fmt.Sprintf("    applyLeapSecond:   %-6v        %-6v\n",
		expectedDateTime.GetDateHasLeapSecond(),
		actualDateTime.GetDateHasLeapSecond())

	outputStr += fmt.Sprintf("isThisInstanceValid:   %-6v        %-6v\n",
		expectedDateTime.IsValidInstance(),
		actualDateTime.IsValidInstance())

	outputStr += fmt.Sprintf("               Year:   %04d         %04d\n",
		expectedDateTime.GetYear(),
		actualDateTime.GetYear())

	outputStr += fmt.Sprintf("              Month:      %02d            %02d\n",
		expectedDateTime.GetMonth(),
		actualDateTime.GetMonth())

	outputStr += fmt.Sprintf("                Day:      %02d            %02d\n",
		expectedDateTime.GetDay(),
		actualDateTime.GetDay())

	outputStr += fmt.Sprintf("               Hour:      %02d            %02d\n",
		expectedDateTime.GetHour(),
		actualDateTime.GetHour())

	outputStr += fmt.Sprintf("             Minute:      %02d            %02d\n",
		expectedDateTime.GetMinute(),
		actualDateTime.GetMinute())

	outputStr += fmt.Sprintf("             Second:      %02d            %02d\n",
		expectedDateTime.GetSecond(),
		actualDateTime.GetSecond())

	outputStr += fmt.Sprintf("         Nanosecond:      %02d            %02d\n",
		expectedDateTime.GetNanosecond(),
		actualDateTime.GetNanosecond())

	return outputStr
}

func GetElapsedTime(elapsedTime time.Duration) string {

	nanosecondsElapsed := int64(elapsedTime)

	if nanosecondsElapsed == 0 {

		return fmt.Sprintf("Nanoseconds: %d\n",
			nanosecondsElapsed)

	} else if nanosecondsElapsed < 0 {
		return "ERROR!"
	}

	resultStr := ""

	temp := int64(time.Minute)
	tempResult := int64(0)

	if nanosecondsElapsed >= temp {
		tempResult = nanosecondsElapsed / temp

		resultStr += fmt.Sprintf("Minutes: %d   ",
			tempResult)

		nanosecondsElapsed -= tempResult * temp
	}

	temp = int64(time.Second)

	if nanosecondsElapsed >= temp {

		tempResult = nanosecondsElapsed / temp

		resultStr += fmt.Sprintf("Seconds: %d   ",
			tempResult)

		nanosecondsElapsed -= tempResult * temp

	}

	temp = int64(time.Millisecond)

	if nanosecondsElapsed >= temp {

		tempResult = nanosecondsElapsed / temp

		resultStr += fmt.Sprintf("Milliseconds: %d   ",
			tempResult)

		nanosecondsElapsed -= tempResult * temp

	} else {

		resultStr += "Milliseconds: 0   "

	}

	temp = int64(time.Microsecond)

	if nanosecondsElapsed >= temp {

		tempResult = nanosecondsElapsed / temp

		resultStr += fmt.Sprintf("Microseconds: %d   ",
			tempResult)

		nanosecondsElapsed -= tempResult * temp

	} else {

		resultStr += "Microseconds: 0   "

	}

	resultStr += fmt.Sprintf("Nanoseconds: %d\n",
		nanosecondsElapsed)

	return resultStr
}
